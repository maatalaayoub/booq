const LanguageContext = createContext();

const locales = ['en', 'fr', 'ar'];

export function LanguageProvider({ children }) {
  const router = useRouter();
  const pathname = usePathname();
  
  // Get locale from URL - computed directly from pathname
  const getLocaleFromPath = useCallback((path) => {
    const segments = (path || '').split('/').filter(Boolean);
    const urlLocale = segments[0];
    return locales.includes(urlLocale) ? urlLocale : 'en';
  }, []);
  
  // Initialize locale from pathname (consistent on server and client)
  const initialLocale = getLocaleFromPath(pathname);
  const [locale, setLocale] = useState(initialLocale);
  
  // Sync locale with URL pathname
  useEffect(() => {
    const urlLocale = getLocaleFromPath(pathname);
    if (urlLocale !== locale) {
      setLocale(urlLocale);
      // Set document direction
      if (typeof document !== 'undefined') {
        if (urlLocale === 'ar') {
          document.documentElement.dir = 'rtl';
          document.documentElement.lang = 'ar';
        } else {
          document.documentElement.dir = 'ltr';
          document.documentElement.lang = urlLocale;
        }
      }
    }
  }, [pathname, locale, getLocaleFromPath]);
  
  const t = (key) => {
    return translations[locale]?.[key] || translations['en']?.[key] || key;
  };
  
  const changeLanguage = useCallback((langCode) => {
    if (!locales.includes(langCode)) return;
    
    setLocale(langCode);
    
    // Set document direction for Arabic
    if (typeof document !== 'undefined') {
      if (langCode === 'ar') {
        document.documentElement.dir = 'rtl';
        document.documentElement.lang = 'ar';
      } else {
        document.documentElement.dir = 'ltr';
        document.documentElement.lang = langCode;
      }
    }
    
    // Navigate to new locale URL
    const segments = pathname.split('/');
    if (locales.includes(segments[1])) {
      segments[1] = langCode;
    } else {
      segments.splice(1, 0, langCode);
    }
    const newPath = segments.join('/') || `/${langCode}`;
    router.push(newPath);
  }, [pathname, router]);
  
  const isRTL = locale === 'ar';
  
  return (
    <LanguageContext.Provider value={{ locale, t, changeLanguage, locales, isRTL }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
